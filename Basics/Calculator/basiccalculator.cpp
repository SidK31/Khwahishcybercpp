#include <iostream>
using namespace std;

int main(){
    cout <<"This is the basic calculator" <<endl;
    int a, b;
    a=10;
    b=30;
    
    cout<< a << "+" << b << "=" << a+b <<endl;    
    cout<< a << "-" << b << "=" << a-b <<endl;   
    cout<< a << "*" << b << "=" << a*b <<endl;
    cout<< a << "/" << b << "/" << a/b <<endl;
    cout<< a << "%" << b << "%" << a%b <<endl;

    return 0;
} 
 